export const getLanguage = (): LanguageCode => {
  const saved = localStorage.getItem('kisanmitra_profile');
  if (saved) {
    try {
      const parsed = JSON.parse(saved);
      if (parsed.language && translations[parsed.language as LanguageCode]) {
        return parsed.language as LanguageCode;
      }
    } catch (e) {
      console.error(e);
    }
  }
  return 'hi'; // Default
};

export const useLanguage = () => {
  const [lang, setLang] = useState<LanguageCode>(getLanguage());

  useEffect(() => {
    const handleUpdate = () => {
      setLang(getLanguage());
    };

    window.addEventListener('storage', handleUpdate);
    window.addEventListener('profile_updated', handleUpdate);

    return () => {
      window.removeEventListener('storage', handleUpdate);
      window.removeEventListener('profile_updated', handleUpdate);
    };
  }, []);

  const t = (key: string, variables?: Record<string, string>): string => {
    const langDict = translations[lang] || translations['hi'];
    let val = langDict[key] || translations['hi'][key] || key;
    if (variables) {
      Object.entries(variables).forEach(([k, v]) => {
        val = val.replace(`{${k}}`, v);
      });
    }
    return val;
  };

  return { lang, t };
};
